						<h2>What We Believe</h2>
						<p>These are nonnegotiable tenets of faith that all Assemblies of God churches adhere to. This list is derived from the official Statement of Fundamental Truths.
						Click links below to see the complete original statement with scriptures.</p>
						<ol style="margin-left: -20px;">
							<li>WE BELIEVE ... <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#1">The Scriptures are Inspired by God</a> and declare His design and plan for mankind.</li>
							<li>WE BELIEVE ... <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#2">There is only One True God</a> &mdash; revealed in three persons ... Father, Son, and Holy Spirit (commonly known as the Trinity).</li>
							<li>WE BELIEVE ... <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#3">In the Deity of the Lord Jesus Christ.</a> As God's son Jesus was both human and divine.</li>
							<li>WE BELIEVE ... though originally good, <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#4">Man Willingly Fell to Sin</a> &mdash; ushering evil and death, both physical and spiritual, into the world.</li>
							<li>WE BELIEVE ... <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#5">Every Person Can Have Restored Fellowship with God Through 'Salvation'</a> (trusting Christ, through faith and repentance, to be our personal Savior). [1 of 4 cardinal doctrines of the AG]</li>
							<li>WE BELIEVE ... and practice two ordinances &mdash; (1) <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#6">Water Baptism by Immersion</a> after repenting of one's sins and receiving Christ's gift of salvation, and (2) <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#6">Holy Communion</a> (the Lord's Supper) as a symbolic remembrance of Christ's suffering and death for our salvation.</li>
							<li>WE BELIEVE ... <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#7">The Baptism in the Holy Spirit is a Special Experience Following Salvation</a> that empowers believers for witnessing and effective service, just as it did in New Testament times. [1 of 4 cardinal doctrines of the AG]</li>
							<li>WE BELIEVE ... <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#8">The Initial Physical Evidence of the Baptism in the Holy Spirit is 'Speaking in Tongues,'</a> as experienced on the Day of Pentecost and referenced throughout Acts and the Epistles.</li>
							<li>WE BELIEVE ... <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#9">Sanctification Initially Occurs at Salvation</a> and is not only a declaration that a believer is holy, but also a progressive lifelong process of separating from evil as believers continually draw closer to God and become more Christlike.</li>
							<li>WE BELIEVE ... <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#10">The Church has a Mission</a> to seek and save all who are lost in sin. We believe 'the Church' is the Body of Christ and consists of the people who, throughout time, have accepted God's offer of redemption (regardless of religious denomination) through the sacrificial death of His son Jesus Christ.</li>
							<li>WE BELIEVE ... <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#11">A Divinely Called and Scripturally Ordained Leadership Ministry Serves the Church.</a> The Bible teaches that each of us under leadership must commit ourselves to reach others for Christ, to worship Him with other believers, to build up or edify the body of believers &mdash; the Church and to Meet human need with ministries of love and compassion.</li>
							<li>WE BELIEVE ... <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#12">Divine Healing of the Sick is a Privilege for Christians Today</a> and is provided for in Christ's atonement (His sacrificial death on the cross for our sins). [1 of 4 cardinal doctrines of the AG]</li>
							<li>WE BELIEVE ... in <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#13">The Blessed Hope &mdash; When Jesus Raptures His Church Prior to His Return to Earth</a> (the second coming). At this future moment in time all believers who have died will rise from their graves and will meet the Lord in the air, and Christians who are alive will be caught up with them, to be with the Lord forever. [1 of 4 cardinal doctrines of the AG]</li>
							<li>WE BELIEVE ... in <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#14">The Millennial Reign of Christ</a> when Jesus returns with His saints at His second coming and begins His benevolent rule over earth for 1,000 years. This millennial reign will bring the salvation of national Israel and the establishment of universal peace.</li>
							<li>WE BELIEVE ... <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#15">A Final Judgment Will Take Place</a> for those who have rejected Christ. They will be judged for their sin and consigned to eternal punishment in a punishing lake of fire.</li>
							<li>WE BELIEVE ... and look forward to the perfect <a target="_blank" href="http://ag.org/top/Beliefs/Statement_of_Fundamental_Truths/sft_full.cfm#16">New Heavens and a New Earth</a> that Christ is preparing for all people, of all time, who have accepted Him. We will live and dwell with Him there forever following His millennial reign on Earth. 'And so shall we forever be with the Lord!'</li>
						</ol>
